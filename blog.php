<p>There are six types of elections in the United Kingdom: United Kingdom general elections, elections to devolved parliaments and assemblies, elections to the European Parliament, local elections, mayoral elections and Police and Crime Commissioner elections.</p>

<p>Elections are held on Election Day, which is conventionally a Thursday. General elections do not have fixed dates, but must be called within five years of the opening of parliament following the last election.</p>

