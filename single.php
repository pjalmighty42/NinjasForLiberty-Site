			<div class="entry-content clearfix">

				<p>There are six types of elections in the United Kingdom: United Kingdom general elections, elections to devolved parliaments and assemblies, elections to the European Parliament, local elections, mayoral elections and Police and Crime Commissioner elections. </p>

				<p>Elections are held on Election Day, which is conventionally a Thursday. General elections do not have fixed dates, but must be called within five years of the opening of parliament following the last election. </p>
				<p>Other elections are held on fixed dates, though in the case of the devolved assemblies and parliaments early elections can occur in certain situations. Currently, six electoral systems are used: the single member plurality system (First Past the Post), the multi member plurality system, party list PR, the single transferable vote, the Additional Member System and the Supplementary Vote.</p>

				<p>Elections are administered locally: in each lower-tier local authority, the actual polling procedure is run by the Returning Officer and the compiling and maintenance of the electoral roll by the Electoral Registration Officer (except in Northern Ireland, where the Electoral Office for Northern Ireland assumes both responsibilities).</p>
						
